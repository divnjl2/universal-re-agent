def create_hme_router():
    """Create a FastAPI router for HME management."""
    from fastapi import APIRouter, HTTPException, Query
    from pydantic import BaseModel

    router = APIRouter()

    class GenerateRequest(BaseModel):
        count: int = 1
        label_prefix: str = "bybit"
        cookie_file: str = "config/icloud_cookies.txt"

    class GenerateResponse(BaseModel):
        emails: List[str]
        count: int

    @router.post("/generate", response_model=GenerateResponse)
    async def generate_hme(body: GenerateRequest):
        """Generate Hide My Email aliases."""
        async with HideMyEmailClient(cookie_file=body.cookie_file) as hme:
            emails = await hme.generate_batch(
                count=body.count,
                label_prefix=body.label_prefix,
                concurrency=min(body.count, BATCH_SIZE),
            )
            return GenerateResponse(emails=emails, count=len(emails))

    @router.get("/list")
    async def list_hme(cookie_file: str = Query("config/icloud_cookies.txt")):
        """List all Hide My Email aliases."""
        async with HideMyEmailClient(cookie_file=cookie_file) as hme:
            result = await hme.list_emails()
            if not result.get("success"):
                raise HTTPException(status_code=502, detail="Failed to list HME")
            return result["result"]

    return router
