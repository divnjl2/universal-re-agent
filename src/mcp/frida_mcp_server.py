"""
Frida MCP Server — JSON-RPC 2.0 server for frida_bridge.py.

Protocol: POST /rpc
  Request:  {"jsonrpc": "2.0", "id": N, "method": "...", "params": {...}}
  Response: {"jsonrpc": "2.0", "id": N, "result": ...}
         or {"jsonrpc": "2.0", "id": N, "error": {"code": -1, "message": "..."}}

Listens on 0.0.0.0:8766 (matches FridaMCPClient default port).

Supported methods:
  ping                           — health check
  tools/list                     — list available tools
  tools/call {name, arguments}   — invoke a Frida tool

Tools:
  generate_hook   — generate Frida Interceptor.attach JS (no Frida needed)
  attach_and_run  — inject script into PID via frida CLI
  spawn_and_run   — spawn binary and inject script via frida CLI
  stalker_trace   — run Stalker-based trace via frida CLI
  memory_scan     — run Memory.scan via frida CLI
"""
from __future__ import annotations

import json
