def _generate_hook(arguments: dict) -> dict:
    """Generate a Frida Interceptor.attach script — purely local, no Frida needed."""
    address   = arguments.get("address", "0x0")
    arg_count = int(arguments.get("arg_count", 4))
    hook_name = arguments.get("hook_name", "auto")
    log_args  = arguments.get("log_args", True)
    log_ret   = arguments.get("log_retval", True)

    if log_args and arg_count > 0:
        args_js = "\n".join(
            f"        console.log('  arg[{i}] =', args[{i}],"
            f" args[{i}].readUtf8String ? tryRead(args[{i}]) : '');"
            for i in range(arg_count)
        )
    else:
        args_js = "        // arg logging disabled"

    ret_js = (
        "        console.log('[<] retval =', retval);"
        if log_ret else
        "        // retval logging disabled"
    )

    script = f"""\
// Frida hook: {hook_name} @ {address}
// Auto-generated by frida-mcp-server

function tryRead(p) {{
    try {{ return p.readUtf8String(); }} catch(_) {{
        try {{ return p.readUtf16String(); }} catch(__) {{ return ''; }}
    }}
}}

const _target = ptr('{address}');
Interceptor.attach(_target, {{
    onEnter: function(args) {{
        console.log('[>] {hook_name} called');
{args_js}
    }},
    onLeave: function(retval) {{
{ret_js}
    }}
}});
console.log('[*] Hook installed: {hook_name} @ {address}');
"""
    return {"script": script, "hook_id": f"hook_{address}"}


def _run_frida(cmd: list[str], timeout: int = 30) -> dict:
    """Execute a frida CLI command and return structured output."""
    if not FRIDA_AVAILABLE:
        return {
            "captures": [],
            "hook_id": "mock",
            "error": (
                "frida CLI not available on this system. "
                "Install with: pip install frida-tools"
            ),
            "mock": True,
        }
    try:
        proc = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=timeout,
        )
        lines = [ln.strip() for ln in proc.stdout.splitlines() if ln.strip()]
        captures = [{"type": "output", "message": ln} for ln in lines]
        error = proc.stderr[:500].strip() if proc.returncode != 0 else None
        return {
            "captures": captures,
            "hook_id": "frida_run",
            "error": error,
        }
    except subprocess.TimeoutExpired:
        return {
            "captures": [],
            "hook_id": "frida_run",
            "error": f"frida process timed out after {timeout}s",
        }
    except Exception as exc:
        return {
            "captures": [],
            "hook_id": "frida_run",
            "error": str(exc),
        }


def _attach_and_run(arguments: dict) -> dict:
    """Attach to a running PID and execute a Frida script."""
    pid    = int(arguments.get("pid", 0))
    script = arguments.get("script", "console.log('hello from frida');")
    return _run_frida(["frida", "-p", str(pid), "--no-pause", "-e", script])


def _spawn_and_run(arguments: dict) -> dict:
    """Spawn a binary and execute a Frida script from process entry."""
    binary = arguments.get("binary_path", "")
    args   = arguments.get("args", [])
    script = arguments.get("script", "console.log('hello from frida');")
    cmd    = ["frida", "-f", binary, "--no-pause", "-e", script]
    if args:
        cmd += ["--"] + [str(a) for a in args]
    return _run_frida(cmd)


def _stalker_trace(arguments: dict) -> list:
    """Run a Stalker-based call summary trace on a PID."""
    pid      = int(arguments.get("pid", 0))
    max_inst = int(arguments.get("max_instructions", 10000))

    stalker_js = f"""\
Stalker.follow(Process.getCurrentThreadId(), {{
    events: {{ call: true, ret: false, exec: false }},
    onCallSummary: function(summary) {{
        for (const [addr, count] of Object.entries(summary)) {{
            const sym = DebugSymbol.fromAddress(ptr(addr));
            send({{type: 'call', address: addr, count: count, sym: sym.toString()}});
        }}
    }}
}});
// Unfollow after 2 seconds
setTimeout(function() {{
    Stalker.unfollow(Process.getCurrentThreadId());
    console.log('[*] Stalker unfollow complete, max_instructions={max_inst}');
}}, 2000);
"""
    result = _run_frida(["frida", "-p", str(pid), "--no-pause", "-e", stalker_js])
    return result.get("captures", [])


def _memory_scan(arguments: dict) -> list:
    """Scan process memory for a byte pattern (Frida wildcard syntax)."""
    pid         = int(arguments.get("pid", 0))
    pattern     = arguments.get("pattern", "")
    range_start = arguments.get("range_start", "0x1000")
    range_size  = int(arguments.get("range_size", 0x7FFFFFFF))

    scan_js = f"""\
Memory.scan(ptr('{range_start}'), {range_size}, '{pattern}', {{
    onMatch: function(address, size) {{
        send({{type: 'match', address: address.toString(), size: size}});
    }},
    onError: function(reason) {{
        console.log('[!] scan error:', reason);
    }},
    onComplete: function() {{
        console.log('[*] Memory scan complete');
    }}
}});
"""
    result = _run_frida(["frida", "-p", str(pid), "--no-pause", "-e", scan_js])
    return result.get("captures", [])


