# ---------------------------------------------------------------------------
# JSON-RPC 2.0 dispatch
# ---------------------------------------------------------------------------

def _jsonrpc_error(req_id: Any, code: int, message: str) -> dict:
    return {
        "jsonrpc": "2.0",
        "id": req_id,
        "error": {"code": code, "message": message},
    }


def _jsonrpc_result(req_id: Any, result: Any) -> dict:
    return {
        "jsonrpc": "2.0",
        "id": req_id,
        "result": result,
    }


def dispatch(request: dict) -> dict:
    """Dispatch a JSON-RPC 2.0 request and return a response dict."""
    req_id = request.get("id")
    method = request.get("method", "")
    params = request.get("params") or {}

    # --- ping ---
    if method == "ping":
        return _jsonrpc_result(req_id, {"pong": True, "frida_available": FRIDA_AVAILABLE})

    # --- tools/list ---
    if method == "tools/list":
        return _jsonrpc_result(req_id, TOOL_DESCRIPTIONS)

    # --- tools/call ---
    if method == "tools/call":
        tool_name = params.get("name", "")
        arguments = params.get("arguments") or {}

        fn = TOOLS.get(tool_name)
        if fn is None:
            return _jsonrpc_error(req_id, -32601, f"Unknown tool: {tool_name!r}")

        try:
            result = fn(arguments)
            return _jsonrpc_result(req_id, result)
        except Exception as exc:
            traceback.print_exc()
            return _jsonrpc_error(req_id, -32000, str(exc))

    # --- unknown method ---
    return _jsonrpc_error(req_id, -32601, f"Method not found: {method!r}")


# ---------------------------------------------------------------------------
# HTTP handler
# ---------------------------------------------------------------------------

class MCPHandler(BaseHTTPRequestHandler):
    """HTTP handler that wraps JSON-RPC 2.0 dispatch on POST /rpc."""

    def log_message(self, fmt: str, *args: Any) -> None:  # type: ignore[override]
        print(f"[frida-mcp] {self.address_string()} — {fmt % args}", flush=True)

    def _send_json(self, status: int, body: dict) -> None:
        data = json.dumps(body, ensure_ascii=False).encode("utf-8")
        self.send_response(status)
        self.send_header("Content-Type", "application/json; charset=utf-8")
        self.send_header("Content-Length", str(len(data)))
        self.end_headers()
        self.wfile.write(data)

    def do_GET(self) -> None:
        if self.path in ("/health", "/"):
            self._send_json(200, {
                "status": "ok",
                "server": "frida-mcp-server",
                "frida_available": FRIDA_AVAILABLE,
                "tools": [t["name"] for t in TOOL_DESCRIPTIONS],
            })
        else:
            self.send_response(404)
            self.end_headers()

    def do_POST(self) -> None:
        if self.path == "/rpc":
            length = int(self.headers.get("Content-Length", 0))
            raw    = self.rfile.read(length)

            try:
                request = json.loads(raw)
            except json.JSONDecodeError as exc:
                self._send_json(400, _jsonrpc_error(None, -32700, f"Parse error: {exc}"))
                return

            print(
                f"[frida-mcp] RPC method={request.get('method')!r} "
                f"id={request.get('id')}",
                flush=True,
            )

            response = dispatch(request)
            self._send_json(200, response)

        elif self.path == "/health":
            self._send_json(200, {
                "status": "ok",
                "frida_available": FRIDA_AVAILABLE,
            })

        else:
            self.send_response(404)
            self.end_headers()


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

def run(host: str = "0.0.0.0", port: int = 8766) -> None:
    print(f"[frida-mcp] Starting Frida MCP server on {host}:{port}", flush=True)
    print(f"[frida-mcp] frida CLI available: {FRIDA_AVAILABLE}", flush=True)
    if not FRIDA_AVAILABLE:
        print(
            "[frida-mcp] WARNING: frida not found in PATH. "
            "generate_hook works locally; attach/spawn/stalker/scan return mocks.",
            flush=True,
        )
    print(f"[frida-mcp] Tools: {', '.join(TOOLS)}", flush=True)
    server = HTTPServer((host, port), MCPHandler)
    try:
        server.serve_forever()
    except KeyboardInterrupt:
        print("[frida-mcp] Stopped.", flush=True)


if __name__ == "__main__":
    _port = int(sys.argv[1]) if len(sys.argv) > 1 else 8766
    run(port=_port)
